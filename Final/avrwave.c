// Tuning word. AKA: "phase increment/step/delta."
// Determines rate at which LUT is stepped through,
// which effects output frequency (Fo).
volatile uint32_t M = 0x00010000;

// Phase accumulator. n = 32.
// Lower 24 bits for fractional resolution.
// Upper 8 bits used to index into LUT.
// Increments by M after each output sample.
// Steps to next LUT value when lower 24 rollover into upper 8.
volatile uint32_t phase_acc = 0x00;


// Configures timer/counter/interrupt.
void Timer1_init();

/**************************** NOTES ****************************
 * F_CPU and OCR1A determine sampling frequency (Fs).
 * --> Fs = F_CPU / OCR1A
 *
 * Fs and M determine output frequency (Fo).
 * --> Fo = (M*Fs) / (2^n)
 *
 * REFERENCE POINT:
 *		    F_CPU = 8MHz
 *		    n = 32
 *		    OCR1A = 122
 *		    Fs = ~65536Hz
 *		    M = 0x00010000
 *		    Fo = ~1Hz
 *
 * Increase/decrease M for ~proportional increase/decrease in Fo.
 ****************************************************************/

// Create a struct to hold all values required
struct waveform 
{
	//Can be 3 different values: L, M, or H.
	//This corresponds to 500Hz, 1kHz, and 5kHz respectively
	char frequency;

	//This represents one of the 4 wave types: sine, triangle, sawtooth, and square
	//0 for sine, 1 for triangle, 2 for sawtooth, and 3 for square
	int type;	
};


/* The functions below are based off of 
 * readtemperature.c code from Bruce Segee:
 * serial_putchar, serial_getchar, serial_stream
 */

void USART_Init(void);
int serial_putchar(char val, FILE *fp);
int serial_getchar(FILE *fp);
static FILE serial_stream = FDEV_SETUP_STREAM(serial_putchar, serial_getchar, _FDEV_SETUP_RW);


volatile struct waveform out;
volatile int id;

int main()
{
    char buffer;
	
	// Configure 8 output pins to 8-bit DAC.
	DDRD |= 0xFC;

	//Initializing calls
	Timer1_init();
    USART_Init();

	//Set the ouput to zero
	id = 4; 
	
	/* Case statment inside while loop to scan in from stdin 
	 * Checks user input and changes M values corresponding 
	 * to the frequency and ID types based on the kind of wave desired
	 */
	WAVE_CONTROL:
		while (1) {
			id = 4;
			fscanf(stdin,"%c", &buffer);
			switch(buffer) {
			    case 'L':
			        out.frequency = 'L';
					M = 37900000;
					break;
			    case 'M':
			        out.frequency = 'M';
					M = 75800000;
					break;
				case 'H':
			        out.frequency = 'H';
					M = 379000000;
					break;
			    case 'S':
			        out.type = 0;
					break;
			    case 'T':
			        out.type = 1;
					break;
			    case 'W':
			        out.type = 2;
					break;
			    case 'Q': 
			        out.type = 3;
					break;
			    case 'B': 
			        goto LOOP;
				default:
					break;
			}
	}
	
	/* Infinite loop where stdin is constantly scanned for the stop command 
	 * and id is set to the type of wave desired
	 */
	LOOP:
		while (1) {
			fscanf(stdin,"%c", &buffer);
			if (buffer == 'P') goto WAVE_CONTROL;
			id = out.type;
		}
}


//Function that initializes the UART Transmission
void USART_Init( void )
{
    /* Baud rate: 2400
     * UBRRn = 8Mhz/(16 * Baud) - 1
     * UBRRn = 207
     * Where UBRRnH is the four most significant bits
     */
    UBRR0H = 0;
    UBRR0L = 207;

    //make sure double transmission speed is disabled
    UCSR0A &= ~(1<<U2X0);

    /* enable transmission and recieving 
     * TXENn is for Transimission
     * RXENn is for Reciving
     */
    UCSR0B = (1<<RXEN0) | (1<<TXEN0);

    // set frame format: 8 data, 2 stop bits    
    UCSR0C = (1<<USBS0) | (3<<UCSZ00);

    stdin=&serial_stream;
    stdout=&serial_stream;
}

// Open the stream to write
int serial_putchar(char val, FILE *fp) {

    // Wait until empty, then write value
    while((UCSR0A&(1<<UDRE0)) == 0);
    UDR0 = val;
    return 0;
}

// Wait until a character is available, then read it
int serial_getchar(FILE *fp) {
    // Wait for a character
    while((UCSR0A&(1<<RXC0)) == 0);
    return UDR0;
}

/* Initialize 16-bit Timer/Counter1.
 * TCNT1 ticks up by 1 every clock cycle (roughly).
 * When TCNT1 = OCR1A, TCNT1 cleared, interrupt triggered.
 */
void Timer1_init()
{
	// Control Registers. Initialize to 0.
	TCCR1A = 0;
	TCCR1B = 0;

	// Timer/Counter1. Initialize to 0.
	TCNT1 = 0;
	
	// Clear Timer on Compare (CTC) match.
	// OCR1A = TOP (max counter value).
	TCCR1B |= (1 << WGM12);

	// Output Compare Register.
	// Determines sampling frequency (Fs).
	// Min OCR1A = ~122 --> Max Fs = ~65536 Hz.
	// May go lower, but nothing changes.
	OCR1A = 122;

	// Output Compare Interrupt Enabled.
	TIMSK1 |= (1 << OCIE1A);

	// System clock. No prescaling.
	TCCR1B |= (1 << CS10);

	// Enable interrupts.
	sei();
}

