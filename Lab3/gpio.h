#include <stdio.h>
#include <stdlib.h>
#include <string.h>
#include <unistd.h>
#include <time.h>

//Exports the GPIO pin for use
int gpio_export(int pin);

//Unexports GPIO pin, removing it from use
int gpio_unexport(int pin);

//controlls the direction of the signal ie. input or output
int gpio_direction(int pin, char *str);

//controls the value of the pin ie. 1 or 0 aka HIGH or LOW
int gpio_value(int pin, int value);

